void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
  
    if(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_6) == 1)
    {
      LED_ON();
    }
    else if(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_6) == 0)
    {
      LED_OFF();
    }

    if(GPIO_Pin == GPIO_PIN_8)
    {
      flags = flags | 0x01;
    }  
}